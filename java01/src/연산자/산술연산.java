package 연산자;

public class 산술연산 {

	public static void main(String[] args) {
		int x = 100;
		int y = 200;
		
		System.out.println("더한 값은 " + (x + y));
		System.out.println("뺀 값은 " + (x - y));
		System.out.println("곱한 값은 " + (x * y));
		System.out.println("나눈 값은 " + (x / y));
		System.out.println("나눈 나머지 값은 " + (x % y));
	}

}
//논리데이터: true/false
//논리연산자: 조건이 여러개일 때  //&&, ||, !

//변수 - 기본데이터(정수, 실수, 문자1, 논리)
//연산자 - 기호(산술, 비교, 논리)
